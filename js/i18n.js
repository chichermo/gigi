  function getVoicePrefix() { return VOICE_PREFIX[currentLang]; }
  function getAppName() { return messages[currentLang].appName; }
  function getBrain() { return messages[currentLang].brain; }

  function setLang(lang) {
    if (!SUPPORTED.includes(lang) || lang === currentLang) return false;
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    if (onChange) onChange(lang);
    return true;
  }

