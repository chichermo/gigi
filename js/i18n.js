  function t(key, vars = {}) {
    let str = messages[currentLang][key] ?? messages.es[key] ?? key;
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    });
    return str;
  }

